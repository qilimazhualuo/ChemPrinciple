export interface BuildItem {
    id: string
    name: string
    icon: string
    /** 贴图路径，相对于 src/assets/buildings/ */
    image: string
    /** 宽度（格子数） */
    width: number
    /** 高度（格子数） */
    height: number
}

export interface BuildCategory {
    id: string
    name: string
    icon: string
    buildings: BuildItem[]
}

function mk(id: string, name: string, icon = '▣', width = 1, height = 1): BuildItem {
    return { id, name, icon, image: `${id}.png`, width, height }
}

