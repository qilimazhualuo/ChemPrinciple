async function downloadOne({ id, name }) {
    const encoded = encodeURIComponent(name)
    const url = `${BASE_URL}/${encoded}.png`
    const outPath = join(ASSETS_DIR, `${id}.png`)
    try {
        const res = await fetch(url)
        if (!res.ok) {
            console.warn(`  [跳过] ${name} (${id}): ${res.status}`)
            return false
        }
        const buf = await res.arrayBuffer()
        await writeFile(outPath, Buffer.from(buf))
        console.log(`  [OK] ${name} -> ${id}.png`)
        return true
    } catch (err) {
        console.warn(`  [失败] ${name} (${id}):`, err.message)
        return false
    }
}

function randomDelayMs() {
    if (REQUEST_DELAY_MIN_MS === REQUEST_DELAY_MAX_MS) return REQUEST_DELAY_MIN_MS
    return (
        Math.floor(Math.random() * (REQUEST_DELAY_MAX_MS - REQUEST_DELAY_MIN_MS + 1)) +
        REQUEST_DELAY_MIN_MS
    )
}

async function hasDownloaded(id) {
    const outPath = join(ASSETS_DIR, `${id}.png`)
    try {
        await access(outPath)
        return true
    } catch {
        return false
    }
}

async function main() {
    await mkdir(ASSETS_DIR, { recursive: true })
    console.log(`下载到 ${ASSETS_DIR}\n`)
    console.log(`每个条目随机间隔: ${REQUEST_DELAY_MIN_MS}-${REQUEST_DELAY_MAX_MS}ms\n`)
    let ok = 0
    let fail = 0
    let existed = 0
    for (const b of buildings) {
        if (await hasDownloaded(b.id)) {
            existed++
            console.log(`  [已存在] ${b.name} -> ${b.id}.png`)
            await new Promise((r) => setTimeout(r, randomDelayMs()))
            continue
        }
        const success = await downloadOne(b)
        if (success) ok++
        else fail++
        await new Promise((r) => setTimeout(r, randomDelayMs()))
    }
    console.log(`\n完成: ${ok} 成功, ${fail} 失败, ${existed} 已存在跳过`)
}

main().catch(console.error)
